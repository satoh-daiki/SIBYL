! =====================================================================
subroutine fread
! ---------------------------------------------------------------------
! Purpose is to read parameters from the file "SUMMARY.out".
! ---------------------------------------------------------------------
! Programed by d.satoh (2022.07.26)
! =====================================================================
  use commondata
! ---------------------------------------------------------------------
  implicit none
! ---------------------------------------------------------------------
  character(len=50) :: cline
  integer :: i
! ---------------------------------------------------------------------
! READ PARAMETER FOR GPM
! ---------------------------------------------------------------------
  open(unit=11,file="SUMMARY.out",status="OLD")

  do i = 1, 5
    read(11,*) cline
  end do

  read(11,'(A36,A1)') cline(1:36), PG

  read(11,'(A19,f10.3)') cline(1:19), Q
  read(11,'(A19,f10.3)') cline(1:19), U
  read(11,'(A19,f10.3)') cline(1:19), H

  do i = 1, 3
    read(11,*) cline
  end do

  read(11,'(A17,i10)') cline(1:17), irs

  read(11,'(A28,i6,A1,i6)') cline(1:28), ns_x_sta, cline(1:1), ns_x_end
  read(11,'(A28,i6,A1,i6)') cline(1:28), ns_y_sta, cline(1:1), ns_y_end
  read(11,'(A28,i6,A1,i6)') cline(1:28), ns_z_sta, cline(1:1), ns_z_end
! ---------------------------------------------------------------------
  close(11)
! ---------------------------------------------------------------------
end subroutine fread